package com.github.shoothzj.dev.util;

import java.util.regex.Pattern;

public class ValidateUtil {

    public static boolean isNotHost(String host) {
        StringBuffer buffer = new StringBuffer();
        buffer.append("^(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9])\\");
        buffer.append(".(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9]|0)\\");
        buffer.append(".(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[1-9]|0)\\");
        buffer.append(".(25[0-5]|2[0-4][0-9]|[0-1]{1}[0-9]{2}|[1-9]{1}[0-9]{1}|[0-9])$");
        Pattern pattern = Pattern.compile(buffer.toString());
        return !pattern.matcher(host).matches();
    }

    public static boolean isNotPort(String port) {
        Pattern pattern = Pattern.compile(
                "^([0-9]|[1-9]\\d|[1-9]\\d{2}|[1-9]\\d{3}|[1-5]\\d{4}|6[0-4]\\d{3}|65[0-4]\\d{2}|655[0-2]\\d|6553[0-5])$");
        return !pattern.matcher(port).matches();
    }
}
